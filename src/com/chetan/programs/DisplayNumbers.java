package com.chetan.programs;

public class DisplayNumbers {
    public static void main(String args[]) {
        int number1 = 10;
        int number2 = 20;
        System.out.println("The First number is :" + number1);//10
        System.out.println("The Second number is :" + number2);//20
        System.out.println(number1+" "+number2);//10 20

        int additionResult = number1 + number2;
        System.out.println("The Sum of number1 and number2 is :" + additionResult);//30
    }
}
